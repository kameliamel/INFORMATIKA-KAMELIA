# Program: Pengenalan Variabel di Python
# Tema: Biodata siswa bernama Kamelia

# Variabel adalah tempat untuk menyimpan data

# Mendeklarasikan variabel
nama = "Kamelia"              # Variabel bertipe string
kelas = "X pplg1"            # String: kelas siswa
umur = 16                    # Integer: umur dalam tahun
tinggi_badan = 145          # Float: tinggi badan dalam cm
siswa_aktif = True            # Boolean: status aktif
hobi = "Melukis"              # String: hobi utama

# Menampilkan isi variabel
print("=== Biodata Siswa ===")
print("Nama         :", nama)
print("Kelas        :", kelas)
print("Umur         :", umur, "tahun")
print("Tinggi Badan :", tinggi_badan, "cm")
print("Hobi         :", hobi)

